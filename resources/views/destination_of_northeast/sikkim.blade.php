                     </p>


                      <p class="paragraph"><strong>Phodong Monastery :</strong> Situated in the Phodong village, some 28 km off Gangtok, this 18th-century monastery is amongst the premier ones in the state to visit. From ancient murals to beautiful paintings, this place offers an insight into Sikkim’s spectacular past.
                     </p>

                     <p class="paragraph"><strong>Aritar :</strong> Known for its natural beauty, temples and monasteries, Aritar is surrounded by lush green forests and beautiful streams.
                     </p>

                     <p class="paragraph"><strong>Do Drul Chorten :</strong> It is one of the most important stupas in Sikkim, nestled in the stunning valleys of Gangtok. Built in 1945 under the supervision of late Truslshi and Rimpoche, the Stupa has around 108 Mani Lhakor or prayer wheels.
                     </p>

                     <p class="paragraph"><strong>Yuksom : </strong> It is an old historic town, which is considered to be amongst the best places to visit in West Sikkim bestowed with natural beauty and uninterrupted trail for trekkers. If you are looking forward to spending some quiet moments in the shadow of nature away from the hustle bustle of the city, then this is it.
                     </p>

                      <p class="paragraph"><strong>Mangan : </strong> The town of Mangan, serving as the capital of the northern part of Sikkim, is a major tourist location and travellers mostly use this town as a base station from where they commence treks and tours to other parts of this district. Mangan is one of the most important and least populated towns in Sikkim.
                     </p>

                      <p class="paragraph"><strong>Tinkitam : </strong> Tinkitam Rayong is a petite village in Sikip Tehsil in South District of Sikkim State. Mount Kanchenjunga can be seen in the backdrop of this town and its natural habitat supports a rare species of orchid, Paphiopedilum Faireanum or lady's slipper.
                     </p>

                     <p class="paragraph"><strong>Chopta Valley : </strong> It is a very famous tourist hotspot in North Sikkim known for its varying hues. Famous as the "Valley of flowers", the Chopta Valley is popular for its riot of colours during summer months. This valley is unexplored and untouched, as a very few tourists travel to this destination.
                     </p>

                     <p class="paragraph"><strong>Thangu Valley :</strong> Thangu, situated at an altitude of around 3900 m, is a beautiful village which is about two hours' drive from Lachen. It is a comfortable village where the tourists usually choose to relax before they proceed to trek to Gurugongmar Lake, Muguthang or Cho Lhamu Lake.
                     </p>

                     <p class="paragraph"><strong>Cholamu Lake : </strong> 14th highest lake in the world and the highest lake in India, the Cholamu Lake in North Sikkim is situated at an altitude of 18000 feet from the sea level. While coming down the slope around 300 feet from Donkiala pass ( 18300 feet )
                     </p>

                     <p class="paragraph"><strong>Borong : </strong> Borong is a small beautiful village near Ravangla in South Sikkim which has recently gained fame due to increasing popularity of village tourism and eco-tourism activities. With Himalayan Mountains sited at the backdrop, Borong is an ideal place for those looking for some peace and tranquility.
                     </p>

                     <p class="paragraph"><strong>Tendong Hill : </strong> It is a destination that offers you a marvelous view of the valley in Sikkim. Situated at an altitude of 8530 ft above Damthang, Tendong Hill is a very famous tourist attraction in the southern part of Sikkim.
                     </p>

                     <p class="paragraph"><strong>Barsey Rhododendron Sanctuary : </strong> Perched at an altitude of 10,000 ft, Barsey is best known for the Barsey Rhododendron Sanctuary, a paradise for nature lovers. The sanctuary itself is a part of the West Sikkim and quite easy to access through road via Hilley, Dentam/Soreng from Geyzing.
                     </p>

                     <p class="paragraph"><strong>Geyzing : </strong> The capital city of the West Sikkim District, Geyzing, also known as Gyalshing is located at an altitude of 6,500 feet above the sea level with lofty and majestic mountains of the Himalayan range surrounding the city. The river Rangeet surges in the western part of the city.
                     </p>

                     <p class="paragraph"><strong>Goecha La :</strong> It is a high mountain pass located at a height of 4,940 m and acts as the base camp for the southeast face of Kanchenjunga. This place can only be accessed by trekking from Yuksom and it roughly takes 7-8 days for a person to reach Goecha La and come back to Yuksom.
                     </p>

                     <p class="paragraph"><strong>Pemayangtse Monastery :</strong> Pemayangtse Monastery is a highly revered Buddhist Monastery situated at Pemayangtse, in the Pelling region of Sikkim. One of the oldest monasteries of the state, it stands tall on a hilltop overlooking the historical Rabdentse ruins, the former capital of the Kingdom of Sikkim.
                     </p>

                     <p class="paragraph"><strong>Biksthang :</strong> It is also famous as Mangalbaray and Chuchen, is a small farmhouse in the Pelling district of Sikkim. It is popular for having a beautiful environment, including the picturesque view of the Himalayas.
                     </p>

                     <p class="paragraph"><strong>Dzongri Trek :</strong> Nestled at an altitude of 4,200 m, Dzongri Trek serves as one of the most popular high altitude trek destination in Sikkim that can be completed in a short time. The trek offers some enchanting sights while hiking through the fascinating trail comprising of varied terrains.
                     </p>

                     <p class="paragraph"><strong>Kanchenjunga National Park :</strong> Named after the third highest mountain peak in the world, and nestled in the lap of the mighty Himalayas in North of Sikkim is the Kanchenjunga National Park. Included recently in UNESCO's world heritage sites, it is a rare high altitude national park and the first "Mixed-Heritage" site.
                     </p>

                     <p class="paragraph"><strong>Kasturi trek :</strong> It is a round trek passing Goecha la pass and offers extraordinary panaromic mountain views. The Trek starts from Yuksom and goes up to Goecha La, the highest point of this trek. From here, you can see the stunning view of Talung Glacier and Mt Kanchenjunga.
                     </p>
