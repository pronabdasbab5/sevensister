                     <h2 class="text-center under">Sikkim</h2>
                     <p class="paragraph">Sikkim is situated at the foot of Mount Kanchendzonga and boasts of an unexplored beauty. The lofty mountains dominate the skyline of Sikkim and invites tourists in its snow clad peaks, green emerald slopes, rushing streams, tall Rhododendrons and sparkling orchids. Hilltop monasteries emerge from the rugged terrains while multi-colored prayer flags wave in the mountain breeze. </p>
                     <p class="paragraph"><strong>North Sikkim :</strong> One of the most uninhabited areas of the country, yet extremely beautiful and charming, North Sikkim shares its boundary with China and Nepal. </p>

                     <p class="paragraph"><strong>West Sikkim :</strong> West Sikkim, located at the foothills of Mount Kanchenjunga, offers one of the best views of Southeast face of Mt. Kangchenjunga. </p>

                     <p class="paragraph"><strong>South Sikkim :</strong> Surrounded by beautiful forests and majestic hills, South Sikkim is the perfect vacation spot for someone looking for a retreat in the midst of nature in its most natural form.</p>

                     <p class="paragraph"><strong>Gangtok :</strong> Gangtok is the capital city of Sikkim and has loads of gems in its kitty; you would be awestruck by the never ending walking trails, gorgeous silver-fir trees and most importantly the hospitality by the locals. Make sure it is on your list of places to visit in Sikkim.</p>

                     <p class="paragraph"><strong>Pelling :</strong> Second most-visited attraction after Gangtok, Pelling will sweep you away with its lush forests and varied wildlife. Since it is a small town, you can easily discover it on foot. It provides a plenty of sightseeing opportunities.</p>

                     <p class="paragraph"><strong>Tsomgo lake :</strong>  Tsomgo lake which is located just 38 km away from the capital city of Sikkim, Gangtok, is among the most popular tourist places in Sikkim. A snaky road through rocky mountain topography and strident mountains which takes you to the Tsomgo Lake is a lot of adventure in itself. If you are looking for best places to visit in East Sikkim then, you must explore this place on your trip.</p>

                     <p class="paragraph"><strong>Rumtek Monastery :</strong> The Rumtek Monastery is one of the largest and most significant monasteries in Sikkim, perched on top of a hill 23 km from Gangtok. Originally called as the Dharma Chakra Centre, it belongs to the Kargyu sect of Buddhists who originated in Tibet in the 12th century.</p>
                     
                     <p class="paragraph"><strong>Nathula pass :</strong>  Nathula pass, located in the Himalayas, connects India with Tibet is considered to be amongst the best places to visit in Sikkim Gangtok. The pass, at 4,310 m above sea level forms a part of a side-shoot of the prehistoric Silk Road. Definitely one of the places to visit in Sikkim without fail.
                     </p>

                     <p class="paragraph"><strong>Gurudongmar Lake :</strong>  Located at the height of 17,100 feet above the sea level, Gurudongmar Lake, located in Sikkim, is one of the fifteen highest lakes in the world. It is also the second highest lake in Sikkim, after the Cholamu Lake located at the height of 18,000 feet.
                     </p>

                     <p class="paragraph"><strong>Lachung, Lachen and Yumthang Valley :</strong>  Lachen is a distant village in North Sikkim and the winter home for Tibetan nomadic tribes. Lachung is also a high altitude village of Tibetan and Bhutia people in North Sikkim. And Yumthang is a beautiful valley bounded by snowcapped mountains. All three are extremely beautiful tourist places in Sikkim. One must visit Lachung as it is the most famous among the list of places to visit in North Sikkim and for experiencing snowfall in Sikkim.
                     </p>

                     <p class="paragraph"><strong>Ravangla :</strong>  Furrowed by dense forests and outstandingly balanced on a ridge superintending an extensive swish of western Sikkim, Ravangla assures one of the most beautiful mountain views. It is a popular tourist place in Sikkim and draws many nature lovers every year.
                     </p>

                     <p class="paragraph"><strong>Namchi :</strong> Namchi is blessed with fresh air which is fragmented with the aroma of lush flora. The beautiful sight of snowcapped mountains and the woodland mountain valleys adds to the scenery and makes it an important spot amongst the places to visit in South Sikkim.
                     </p>

                     <p class="paragraph"><strong>Zuluk :</strong> One of the offbeat Sikkim tourist places, Zuluk is a small village located at a high altitude and has a very empowering history due to its connection with the Silk Route. It promises great views of the snowcapped mountain ranges especially during winter and is a fabulous place to see for those looking for snowfall in Sikkim or looking for the best places to visit in Sikkim and Darjeeling.
                     </p>

                     <p class="paragraph"><strong>Teesta River :</strong> A different one among places to visit in Sikkim, Teesta River is stunning to look at when it is frozen in winters. During spring, however, the river is shimmering in the sun rays and displays a perfect habitat for blooming flowers.
                     </p>

                     <p class="paragraph"><strong>Singalila National Park :</strong> The Singalila is amongst those beautiful places to visit in Sikkim that even a non-wildlife enthusiast would love to explore. Offering breathtaking view of Kanchenjunga and Mount Everest peaks, it is a place you definitely can’t miss.
                     </p>

