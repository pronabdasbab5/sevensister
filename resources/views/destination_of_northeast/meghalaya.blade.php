                     <h2 class="text-center under">Meghalaya</h2>
                     <p class="paragraph">The Meghalaya attractions include mainly the innumerable tourist places that form the backbone of Meghalaya tourism. Some of the major places of interest of Meghalaya are Krem Mawmluh, Krem Phyllut, Mawsynram, Mawsmai and Siju.</p>

                     <p class="paragraph"><strong>Shillong :</strong> Born in the laps of nature, Shillong is the capital of Meghalaya and offers every one of its bequeathed element for exploration to the wanderlust. Literally translating into an ‘abode of clouds’, Shillong is a breathtaking hill station which is nestled amidst the paradisiacal Garo and Khasi hills. With sparkling waterfalls, manicured gardens, azure lakes, and distinct culture, this alluring hill town beckons you to witness the natural charm of Northeast mesmerizing places to visit in Shillong. Referred as the ‘Scotland of the East’, the natural scenery of Shillong has a striking similarity with the mountains in the east.</p>
                      <p class="paragraph">The best time for Shillong tour is from the months of September to May. During these months, the weather is extremely pleasant with moderate rainfall. Shillong looks beautiful and invigorating during the monsoon season (June-August), but torrential rain may disrupt travel plans.</p>

                     <p class="paragraph"><strong>Cherrapunjee :</strong> The wettest place on the Earth, Cherrapunjee with its clean and pristine surroundings is an excellent place to sit back and unwind. Cherrapunjee, also known as Sohra, is a high-altitude town in the northeast Indian state of Meghalaya. It's known for its living root bridges, made from rubber trees. To the northeast, Mawkdok Dympep Valley View Point overlooks verdant gorges. In the surrounding hills, the Noh-Kalikai, Dain-Thlen and Kynrem waterfalls plunge from forested clifftops. South of town, Mawsmai Cave is illuminated to show its stalagmites.</p>

                     <p class="paragraph"><strong>Mawsynram:</strong>Acknowledged as one of the wettest place in the world, the mesmerizing village of Mawsynram trounces Cherrapunjee by a slight margin in being the wettest place in India. According to the Guinness Book of World Records, Mawsynram received 26,000 millimetres (1,000 in) of rainfall in 1985.</p>
                     <p> Three reasons can be cited for high rainfall at Mawsynram:
                        <ul class="ull">
                        <li>• The warm moist winds of the northward-moving air from the Bay of Bengal during the monsoon, which cover an extensive area but are forced to converge into the narrower zone over the Khasi Hills, thus concentrating their moisture.</li>
                        <li>• The alignment of the Khasi Hills (east to west) places them directly in the path of the airflow from the Bay of Bengal, producing a significant uplift (plus cooling, further condensation and thus more rain).</li>
                        <li>• Finally, uplift over the Khasi Hills is virtually continuous in the monsoon period because the lifted air is constantly being pulled up by vigorous winds in the upper atmosphere; hence the rainfall is more or less continuous.</li>
                     </ul>
                     </p>

                     <p class="paragraph"><strong>Tura :</strong> Located in the West Garo hills, Tura provides a spellbinding as well as serene environment as it is quite an unexplored town. This relatively large town in Meghalaya is located in the West Garo Hills. The main attraction of this town is the Nokrek National Park which is 12km from the town where various animals such as the leopard, golden cat, wild buffalo, pheasant and many more find habitat. One may also visit the Rongbang Dar Falls, while a trip to this area, without visiting the Siju caves, would remain somewhat incomplete. The entire area has the simple nature of an unexplored destination, with a shifted world.</p>

                     <p class="paragraph"><strong>Jowai : </strong> Located in the Jaintia Hills district, Jowai is famous for its scenic setting and breathtaking views with the perfect mix of heritage and culture. The place is a rich mix of heritage and cultures which reflects in the lifestyle of the town folks. Lakes are the main attraction of Jowai. The Thadlaskein Lake and Lalong Park are the famous tourists’ hot spots while Syntu Ksiar is also a popular destination on the shores of river Myntdu.</p>

                     <p class="paragraph"><strong>William Nagar :</strong> It was formed around the former town of Simsanggre, which is based on the large plains of the Simsang River.</p>

                     <p class="paragraph"><strong>Baghmara : </strong> Situated in Meghalaya, Baghmara is not only rich in flora and fauna, but it is enveloped by lakes, rivers, hills and every significant element of the nature.</p>

                     <p class="paragraph"><strong>Double Decker Living Root Bridge :</strong> Hidden deep in the lush, green forests of the wettest place in India: Cherrapunjee, Meghalaya; one can find the magnificent Jingkieng Nongriat Double Decker Living Root Bridge; more commonly known as the Umshiang Double Decker Living Root Bridge or simply just the Double Decker Root Bridge. 
                     </p>

                     <p class="paragraph"><strong>Mawsmai Cave :</strong> Meghalaya is home to some amazing and mysterious cave systems and Mawsmai Cave is by far the most popular of the lot. Located just 6 km away from the heart of Cherrapunjee Mawsmai cave is a breathtaking maze of caves in the East Khasi Hills of Meghalaya. </p>

                     <p class="paragraph"><strong>Nohkalikai falls :</strong> The fourth highest waterfalls in the world, Nohkalikai falls plunge some 335 meters from a verdant cliff to the ground giving the portrayal of immense and natural magnificence. The pride of the state of Meghalaya, the falls are one of the most beautiful and grand waterfalls in the country.</p>

                     <p class="paragraph"><strong>Dawki :</strong> A small border town in the Jaintia Hills, Dawki and its famed river Umngot is a must visit if in Meghalaya. Sandwiched between India and Bangladesh, Dawki is a major center of trade between India and Bangladesh. The small city, besides being a trade hub is also famous for its tourism scene.</p>

                     <p class="paragraph"><strong>Jaintia Hills :</strong> Forming the southern, eastern and northern part of the state Jaintia Hills are a sacred, pristine patch of hillocks and forest whose beauty and freshness is unparalleled. The part of the erstwhile Jaintia Kingdom the Jaintia Hills are dotted with famous monoliths of stones, unscarred forests.</p>

                     <p class="paragraph"><strong>Elephant Falls :</strong> Named after an Elephant like stone at its foot, the Elephant Falls are amongst the most popular falls in the North-East, situated next to Shillong. It is a tourists' paradise with three layers of the falls accessible to the layman from different vantage points.</p>

                     <p class="paragraph"><strong>Mawlynnong :</strong> It is a small village in the East Khasi Hills district in Meghalaya, the abode of changing clouds. Mawlynnong is famous for its matrilineal society as well as having been declared Asia's cleanest village which comes under the Pynursla community development block.</p>

                     <p class="paragraph"><strong>Umiam Lake :</strong> Umiam Lake, a mesmerizing man-made reservoir, is located at a distance of 15 kilometres north of Shillong which is the capital of the north-eastern Indian state of Meghalaya. The lake was formed after a dam was constructed to generate hydroelectric power.</p>

                     <p class="paragraph"><strong>Kyllang Rock :</strong> Some 78 km away from Shillong, a unique giant rock made out of red granite, Kyllang Rock is a giant dome placed in the West Khasi Hills of Meghalaya. The Rock which is 5400 ft above sea level and has a width of around 1000 ft is a mammoth block of granite which is part of the Khasi folklore.</p>

                     <p class="paragraph"><strong>Nohsngithiang Falls :</strong> The Nohsngithiang Falls, located at a distance of about 4km from Cherrapunjee, is one of the tallest waterfalls in India and a major tourist spot in Cherrapunjee. Since it is situated in the Mawsmai village of the East Khasi Hills District of Meghalaya, it is also known as Mawsmai Falls.</p>

                     <p class="paragraph"><strong>Balpakram National Park :</strong> Located near the Garo Hills in Meghalaya in India about 3,000 feet above the sea level, the Balpakram National Park is a famous national park which is well known for its pristine beauty. The word Balpakram means the 'land of perpetual winds'.</p>

                      <p class="paragraph"><strong>Garo Hills :</strong> The Garo Hills form the west part of the state and are known for the famous Garo tribes whose belief in nature and folklore are as enchanting as the verdant hills they live in. The Hills are famous for being home to some great waterfalls too.</p>
