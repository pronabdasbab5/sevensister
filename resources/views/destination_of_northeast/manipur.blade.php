                     <h2 class="text-center under">Manipur</h2>
                     <p class="paragraph">Coyly nestled in the northeast corner of India, the lush green state of Manipur aptly deserves the name, which in Hindi means "the jewel of a land". The picturesque landscape is known to the world more because of its divine Manipuri dance form. 
                     </p>
                     <p class="paragraph">Set among the smoky blue hills, tiny state of Manipur is a land of myths and mystery. Tourist attractions in Manipur lie mostly in its splendid landscape, bounteous nature and its cultural wealth. Smokey blue hills and the verdant forests, offer a stunning combination, which you can hardly ignore. Nature is intoxicatingly beautiful and the serenity is overwhelming. 
                     </p>

                     <p class="paragraph"><strong>Loktak Lake and Sendra Island :</strong>  This is the largest fresh water lake in the North-Eastern India ad the only floating lake in the world. Few islands including Bhumia and Sendra and situated in the lake. Also the Keibul National Park is situated in this lake.</p>

                     <p class="paragraph"><strong>Kangla Fort :</strong> Also known as the Palace of Kangla, this place situated on the banks of the Imphal river, served as the royal palace of King Pakhangba. After 2003 this fort has been open for public viewing. It has historical as well as archaeological significance which makes it a great tourist attraction.</p>

                     <p class="paragraph"><strong>Tharon Cave :</strong> A 655.6 m long cave, this cave can be explored inside out by enthusiasts, with the help of the map drawn outside the cave. It attracts tourists interested in an adventurous experience. It is also spelled as the Thaeuluan Cave.</p>

                     <p class="paragraph"><strong>Shaheed Minar :</strong>  This 11 m tall tower is situated in the centre of the Bir Tirkendrajit Park and is a memorial for those who died against the British while fighting in the Meitei Revolt in 1891.</p>

                     <p class="paragraph"><strong>INA Memorial :</strong> Once used as the unofficial headquarters of the Indian National Army, this memorial was built to honour the soldiers who had sacrificed their lives for India. Netaji Subhash Chandra Bose motivated and formed armies here to participate in the freedom struggle.</p>

                     <p class="paragraph"><strong>Keibul Lamjao National Park : </strong>The only floating National Park in the world, this place is definitely a must see. It is home to various species of plants and animals, including the migratory visitors during the winters.
                     </p>

                     <p class="paragraph"><strong>Langthabal :</strong>  One of the major attractions of the city, Langthabal is most famous for its magnificent temples and historical monuments. The jackfruit and pine plantations among the splendid architectural beauties are a must see.</p>

                     <p class="paragraph"><strong> Singda Dam :</strong> The highest mud dam in the world is the Singda Dam. Singda, located 16 km from the capital city of Imphal, is located at a height of about meters above sea level. It is a popular picnic spot, with its scenic views, and relative calm.</p>

                     <p class="paragraph"><strong> Manipur Zoological Garden :</strong> Specialised in the preservation and breeding of rare species, this place with its lush greenery and calm atmosphere is surrounded by hills and was established in 1976.</p>

                     <p class="paragraph"><strong> Shri Govindjee Temple :</strong> Situated near the royal palace, this temple is one of the major ones in Manipur and is constructed in the Nagara style of architecture. It is dedicated to Shree Govindajee along with various other Hindu Gods and Goddesses viz Krishna, Balaram, Jagannath, Subhadra etc.</p>

                     <p class="paragraph"><strong> Manipur State Museum :</strong> Set up in 1969, this museum caters to various categories of artifacts such as Ethnology, Archaeology, Natural History and Painting. It showcases articles owned by the Manipuri rulers.</p>

                     <p class="paragraph"><strong> Khongjom :</strong> This historically important place marks the site where Major General Paona along with his soldiers fought against the British invasion, ending in complete British control over Manipur.</p>

                     <p class="paragraph"><strong> Ebudhou Thangjing Temple, Moirang :</strong> Located in Bishnupur, Moirang is famous for its rich cultural background. During May, when the Moirang Lai Haraoba festival is celebrated, people flock to seek the blessings of 'Ebudhou Thangjing'.</p>
                     <p class="paragraph"> Other places that can be visited in Imphal are Waithou Lake, War cemeteries, Andro, Chorus Repertory Theater, Kaina and Khonghampat Orchidarium.</p>

                     <p class="paragraph"><strong> Phubala :</strong> Located near the western borders of the Loktak Lake, Phubala has many attractions. Water sports can be carried out at the two islands that are situated amidst the Lake. A picturesque view of the lake can be enjoyed by the tourists.</p>

                     <p class="paragraph"><strong> Moirang :</strong> Moirang is a small town nestled in the state of Manipur. This place has seen wars and upheavals in the past but is beautiful nonetheless. Moirang is famous for the Loktak Lake whose serene water is home to plethora of flora and fauna. This place is perfect for a weekend getaway to unwind and relax.</p>
