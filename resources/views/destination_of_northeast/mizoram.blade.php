                     <p class="paragraph">It is a state sandwiched between the mighty Himalayan foothills and the plateau of Meghalaya. Aizawl is the capital of Mizoram, dotted with quaint timber houses and a profusion of flowers. Each house has its own window box of profusely blooming orchids. The air is invigorating and pure. The streets echo with melodies emanating from the guitars of Aizawl youth playing at nearly every street corner.  
