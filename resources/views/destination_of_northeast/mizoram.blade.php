                     <p class="paragraph"><strong>Vantawng falls :</strong> The highest waterfall in the state and the 13th highest in the country, the Vantawng Waterfalls is one of he most mesmerizing waterfalls of the North East. Vantawng falls are the pride of Mizoram and one of its prime attractions. The waterfall which is located 137 km away from the capital city.</p>

                     <p class="paragraph"><strong>Falkawn Village :</strong> A model village depicting the lifestyle of the Mizo people, Falkawn village is a unique cultural extravaganza for first time visitors to Mizoram. Falkawn village depicts the livelihoods and cultures of the Mizo people, who form the biggest tribe of the state. Located 22 km away from the capital city.</p>

