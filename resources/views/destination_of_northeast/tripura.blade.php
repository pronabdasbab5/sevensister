                     <h2 class="text-center under">Tripura</h2>
                     <p class="paragraph">Tripura has a picturesque surrounding covered with hills and dales, sprawling green valleys and hilly brooks. The popular Tripura attractions are Kamalasagar Lake, Dumboor Lake, Ujjayanta Palace, Neermahal, Kunjaban Palace, Tripura Government Museum, Jampui Hill, Dumboor Lake etc. </p>

                     <p class="paragraph"><strong>Ujjayanta Palace :</strong>  Ujjayanta Palace was a royal palace. Situated at the core of Agartala, the entire hustle-bustle of the city is centered around this palace. Built in 1901, it has magnificent tiled floors, curved wooden ceilings and lovely doors. The name 'Ujjayanta Palace' was given by Rabindranath Tagor.</p>

                     <p class="paragraph"><strong>Neermahal :</strong>  Standing in the middle of the Rudrasagar Lake, this architectural beauty rightly derives its name which means Water Palace. This place served as a summer resort for King Maharaja Bir Bikram Kishore Manikya in 1930. </p>

                     <p class="paragraph"><strong>Tripura Sundari Temple :</strong> Located in Matabari, 100m east of NH 44, around 55km from Agartala and just 4 km from Udaipur (Tripura), this temple is one of the most famous temple of Tripura. </p>

                     <p class="paragraph"><strong>Sepahijala Wildlife Sanctuary :</strong> Home to a variety of wildlife especially birds and primates, this place is not just a wildlife sanctuary but also an academic and research centre. Various lakes are present within the sanctuary, where boating facility is available.</p>

                     <p class="paragraph"><strong>Chittangong Hills :</strong> The picturesque spectacle of the Chittagong hills assaults the senses of each and every onlooker. The hills comprise of mini mountains with multiple valleys and a small canyon, with 7 rivers meandering across the valleys.</p>

                     <p class="paragraph"><strong>Ummaneshwar Temple :</strong>  Located within the Ujjayanta palace grounds, this saffron coloured temple is influenced by West Bengali culture. 
                     </p>

                     <p class="paragraph"><strong>Jagannath Temple :</strong> The Jagannath Temple is built within the Ujjayanta Palace grounds, specially crafted to decorate the Manikya capital city. Islamic style of architecture is predominant in the exterior of this temple, but the interiors are decorated with Hindu splendor.
                     </p>

                     <p class="paragraph"><strong>Gondacherra Wildlife Sanctuary :</strong> This enchanting sanctuary has maintained an untainted habitat for the multiple species of flora and fauna found here. Mammals like tigers, bison, wild horses, aquatic life as well as migratory birds can be seen here.
                     </p>

                     <p class="paragraph"><strong>Buddha Temple :</strong> Evidence to the Buddhist influence on the people of Agartala, the Buddha Temple is an important religious site and was constructed in 1946.
                     </p>

                     <p class="paragraph"><strong>Jampui hills :</strong> Also known as the eternal hills of spring, the Jampui hills live up to their name, having pleasant climate all year round, providing ample opportunity for each visitor to take in the beauty that is this place.
                     </p>

                     <p class="paragraph"><strong>Raima Valley :</strong> Known as the Mother of the Tribals of Tripura, this lush green valley, decorated with gardens and plantations has become a preferred tourist spot.
                     </p>

                     <p class="paragraph"><strong>Kunjaban Palace :</strong> Constructed by King Birendra Kishore Manikya in 1917, the Kunjaban Palace is the official residence of the Governor of Tripura.
                     </p>
