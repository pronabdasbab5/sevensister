                     <h2 class="text-center">DIBRU SAIKHOWA NATIONAL PARK</h2>
                     <p class="paragraph">Dibru Saikhowa is one of the bio diversity hotspots situated in the south bank of the river Brahmaputra in the extreme East of Assam state in India. Dibru Saikhowa, which having an area of 340 sq km, is among the most vibrant wildness on earth and is also distinct for her pristine scenic beauty. Dibru Saikhowa wildlife sanctuary was declared as a National Park in 1999.  Situated in the flood plain of Brahmaputra, Dibru Saikhowa is a safe haven for many extremely rare and endangered species of Wildlife. </p>
                     <p class="paragraph">The forest type of Dibru Saikhowa comprises of semi evergreen forests, deciduous forests, littoral and swamp forests and patches of wet evergreen forests.</p>
                     <p class="paragraph">Famed for Feral horses, a total 36 species of mammals and above 400 species of birds have so far been recorded from the Dibru – Saikhowa National Park.</p>
                     <p class="paragraph">Dibru Saikhowa National Park is divided into nine distinct zones one of which is a wetland while dense forests occupy the remaining portions. The sparkling sylvan wetlands paint a picture of rare beauty as the gleam like a mirage of quicksilver under the playful saffron sunbeams. </p>
                     <p><strong>Climate:</strong></p>
                     <p class="paragraph">The climate is tropical. Summers are hot and wet whereas winters are cold and dry.</p>
                     <p><strong>Flora:</strong></p>
                     <p class="paragraph">Tetrasperma, Dillenia indica, Bombax ceiba, Lagerstroemia parviflora, Terminalia myriocarpa, Mesua ferrea, Dalbergia sissoo, Arundo donax, Imperata cylindrical, Phragmaties karka, Erianthus ravanea etc.</p>
                     <p><strong>Mammals:</strong></p>
                     <p class="paragraph">Feral Horses, Hoolock gibbon, Capped langur, Slow ioris, Water buffalo, Tiger, Elephant, Gangetic river dolphin, Leopard, Clouded Leopard, Jungle cat, Sloth bear, Dhole, Small Indian Civet, Malayan Giant Squirrel, Chinese Pangolin, Pig tailed macaque,  Assamese macaque, Rhesus macaque, Wild boar, Sambar, Hog deer and Barking deer etc.</p>
                     <p><strong>Birds:</strong></p>
                     <p class="paragraph">White – winged wood duck, Spot billed pelican, White billed heron, Fishing Eagle, Pale capped pigeon, Swamp francolin, Geyleg goose, Brahmini duck, Bar- headed goose, Pelican, Lesser adjutant stork, Great Grebe, Indian Shag, Grey Heron, Purple Heron, Night Heron, Yellow bittern, Open bill stork, Black necked stork, Crested serpent eagle, Greater spotted eagle, Bengal florican etc. </p>
                     <p><strong>Reptiles:</strong></p>
                     <p class="paragraph">Dibru Saikhowa is uniformly rich and diver in herpetofauna. Two species of Monitor lizards, eight species of Turtles and eight species of Snakes have so far been recorded from Dibru Saikhowa.</p>
                     <p><strong>Best time to visit:</strong></p>
                     <p class="paragraph">November to April is the best season to visit Dibru Saikhowa National Park.</p>
                     <p class="text-center"><strong>How to reach</strong></p>
                     <p class="paragraph"><strong>By Air – </strong>Nearest airport is Mohanbari, Dibrugarh airport, about 40 km from the Dibru Saikhowa national park. </p>
                     <p class="paragraph"><strong>By Road – </strong>Distance from Tinsukia town is 10 km and from nearest Dibrugarh airport is 54 km. From Guwahati the distance is 510 km, 12 hours by road.</p>
                     <p class="paragraph"><strong>By Train – </strong>Nearest railway station is Tinsukia railway station.</p>

                     <p><strong>Travel Tips:</strong></p>
                     <p class="paragraph">Permission from Forest Department is required for entry into the park. Guijan Ghat (10 km from Tinsukia) and Saikhowa Ghat are the two entry points for tourists. Entry before Sunrise and after Sunset as well as night halt and picnic party inside the park is not allowed. There are two forest villages Dhadia and Laika inside the park. </p>
                     <p class="paragraph">Hiring a Local Guide is highly recommended. They are a mine of information on the park and have adequate knowledge about the concentration of wildlife, making for a rewarding trip.</p>
                     <p class="text-center"><strong >Activities</strong></p>
                     <p class="paragraph"><strong>Boat Safari – </strong>There are no Elephant or Jeep safari in Dibru Saikhowa National Park and the visitors have to hire a local boat to explore the park. </p>
                     <p class="paragraph"><strong>Trekking – </strong>Another rewarding way of exploring this national park is by way of trekking. The jungle is quite far away from the cacophony of the city and the stillness is only broken by the rustle of dry leaves beneath your feet or the chorus of birdcalls. Your guide will lead you down the forest trails.</p>
                      <p class="paragraph">It is also possible to explore the two villages inside the park, Dhadia and Laika and see how the locals live. Or you could head to Maguri Beel, adjacent to the park, where a variety of birds can be spotted.</p>
