                     <h2 class="text-center">Nameri NATIONAL PARK</h2>
                     <p class="paragraph">Nameri National Park is located in the foothills of the Eastern Himalayas in the Sonitpur District of Assam, India, about 40 kilometres from Tezpur and the third national park in Assam. The Pakhui (Pakke) Sanctuary of Arunachal Pradesh adjoins the Park on its North-Eastern point. Together they constitute an area of over 1000 km 2 of semi-evergreen, moist deciduous forests with cane and bamboo brakes and narrow strips of open grassland along rivers. Nameri is a birder's paradise with over 300 species of birds.</p>
                     <p class="paragraph">The area is criss-crossed by the river Jia- Bhoroli and its tributaries namely the Diji, Dinai, Doigurung, Nameri, Dikorai, Khari etc. A few jheels (during the rainy seasons) also dot the area. The inaccessibility and continuity with the neibouring forest areas have helped the wildlife of Nameri to flourish. There is a good prey base in the form of Sambar, Barking deer, Hog Deer, Wild Boar and Gaur. About 3000 domestic cattle also form part of this prey base for Tiger and Leopards.</p>
                     <p class="paragraph">Nameri and Sonai-Rupai are only Protected Areas in the North Bank of the Brahmaputra in the Civil District of Sonitpur.</p>

                     
                     <p><strong>Habitat – Flora Fauna of NAMERI NATIONAL PARK:</strong></p>
                     <p class="paragraph">The habitat of Nameri is made up of tropical evergreen, Semi-evergreen, moist deciduous forests with cane and bamboo brakes and narrow strips of open grassland along rivers. Grassland comprise of less than 10% of the total area of the park while the semi-evergreen and moist deciduous species dominate the area. Some notable species are Gamari, Titachopa, Amari, Bogipoma, Ajar, Urium poma, Bhelou, Agaru, Rudraksha, Bonjolokia, Hatipolia akhakan, ahollock, Nahor, Siya Nahar, Simul, Bonsum etc. Orchoids include Dendrobium, Cymbidium, Ladies Sleeper etc. Tree Fern,Lianas, creepers are some of the specialties of this forests.</p>
                     <p class="paragraph">The most prized and most significant finding is the White Winged Wood Duck which has a flourishing population in Nameri confirmed officially in 1995. Till now 315 avian species have been recorded in the Park.</p>
                     <p><strong>Birds (Approx 315 species found):</strong></p>
                     <p class="paragraph">White winged wood duck, Great Pied Hornbill, Wreathed Hornbill, Rufous Necked Hornbill, Black Stork, Ibis Bill, Large Whistling Teal, Common Mergernser, King Vulture, Long Billed Ring Plover, Khallej Pheasant, Hill Myna, Pin tailed green Pigeon, Himalayan pied Kingfisher, Three Toed kingfisher, Fairly Blue Bird etc.</p>
                     <p><strong>Reptiles:</strong></p>
                     <p class="paragraph">King cobra, cobra, Pit Viper, Russels Viper, Banded Krait, Python, Rat Snake, Assam Roof Turtle, Malayan Box Turtle, Keeled Box turtle, Asian Leaf Turtle, Narrow Headed soft Shelled Turtle, Indian soft Shelled Turtle, etc.</p>
                     <p><strong>Fishes:</strong></p>
                     <p class="paragraph">Golden Mahseer, Shaort gilled Mahseer, Silghoria, etc</p>
                     <p><strong>Mammals (More than 30 species found):</strong></p>
                     <p class="paragraph">Tiger, Leopard, Black Panther, Clouded Leopard, Lesse Cats, Sloth Bear, Himalayan Black Bear, Elephant, Indian Bison, Dhole, Sambar, Barking Deer, Dog Deer, Fox, Hispid Hare, Indian Hare, Capped Langur, Slow Loris, Assamese Macaque, Rhesus Macaque, Himalayan yellow Throated Martin, Malayan giant Squirrel, Flying Squirrel, Wild Pig etc.</p>
                     <p><strong>How to reach Nameri National Park:</strong></p>
