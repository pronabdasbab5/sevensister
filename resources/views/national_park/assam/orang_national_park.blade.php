                     <h2 class="text-center">ORANG NATIONAL PARK</h2>
                     <p class="paragraph">Resting at the north bank of the Brahmaputra, the Orang national park is commonly known as the “Mini Kaziranga” for its resemblance with the landscape of the Kaziranga National Park. The Orang National Park also known as Rajiv Gandhi Orang National Park which is located on the north bank of the Brahmaputra River in the Darrang and Sonitpur districts of Assam, India, covers an area of 78.81 square kilometers (30.43 sq mi). It was established as a sanctuary in 1985 and declared a national park on 13 April 1999.The park has a rich flora and fauna, including great Indian one-horned rhinoceros, pygmy hog, elephants, wild buffalo and tigers. It is the only stronghold of rhinoceros on the north bank of the Brahmaputra River.</p>
                     <p><strong>History:</strong></p>
                     <p class="paragraph">The park was established as a wild life sanctuary in 1985, vide notification No. FRS 133/85/5 dated 20 September 1985. But in 1992, the park was renamed as Rajiv Gandhi Wildlife Sanctuary but this action had to be reversed due to public pressure against the renaming. Finally, the sanctuary was declared as National Park in 1999 vide notification No. </p>
                     <p><strong>How to reach Orang National Park:</strong></p>
                     <p class="paragraph">Orang is at a distance of 140 km from Guwahati, 32 km from Tezpur and 68 km from Mangaldai, the district HQ by road. It would take one and half hour to reach the Orang National Park from Mangaldai. The nearest railhead is Rongapara and the nearest airport is Saloni, Tezpur. Public buses ad rental cars are available from Guwahati and other nearby cities.</p>
                     <p><strong>Fauna:</strong></p>
                     <p class="paragraph">Orang contains significant breeding populations of several mammalian species. Apart from the great Indian one-horned rhinoceros (68 at the last count), which is the dominant species of the national park, the other key species sharing the habitat are the royal Bengal tiger (Panther Tigris), Asiatic elephant, pygmy hog, hog deer and wild boar. Some important species of the critically endangered and endangered category are the following.</p>
                     <p class="paragraph">The pygmy hog, a small wild pig, is critically endangered, as per IUCN listing, and is limited to about 75 animals in captivity, confined to a very few locations in and around north-western Assam, including the Orang National Park where it has been introduced. Other mammals reported are the blind dolphin, Indian pangolin, hog deer, rhesus macaque, Bengal porcupine, Indian fox, small Indian civet, otter, leopard cat, fishing cat and jungle cat.</p>
                     <p class="paragraph">The Bengal tiger population was estimated to comprise 19 individuals in 2000.</p>
                     <p class="paragraph">The great Indian rhinoceros even though well conserved now in many national parks and in captivity, is still in the endangered list of IUCN and its population is estimated at 68, as per census carried out by the forest department, in 2006.</p>
                     <p><strong>Flora:</strong></p>
                     <p class="paragraph">The park has rich vegetation of forests, natural forest, and non-aquatic grass/plants. The forest species found are Bombax ceiba, Dalbergia sissoo, Sterculia villosa, Trewia nudiflora, Zizyphus jujuba and Litsaea polyantha. Among the non aquatic grassland species the prominent are Phragmites karka, Arundo donax, Imperata cylindrica and Saccharum spp. The aquatic grass/plants species found are: Andropogon spp., Ipomoea reptans, Enhydra fluctuans, Nymphaea spp. and Water hyacinth.</p>
                     <p><strong>Birds:</strong></p>
                     <p class="paragraph">Orang National Park is Assam is the most important habitat of the Bengal Florican. The vacation tours of this wildlife park will help you to sight the estimated population is about 30-40 birds and other birds are like Spot - billed Pelican, Black – necked stork, Greater adjutant stork, Lesser adjutant, Wooley – necked stork, Rudy shelduck, Gadwall, Mallard, Pintail, Pallas’s Fishing Eagle and a no of others including woodland and grassland birds. </p>
                     <p><strong>Reptiles:</strong></p>
                     <p class="paragraph">Among reptiles, turtles like Lissemys punctata, Kachuga tecta and among snakes Pythons, Cobras are known to frequent in this National Park.</p>
                     <p><strong>Trip within the Orang National Park:</strong></p>
                     <p class="paragraph">It is possible to tour the park interiors on the back of the Elephant.</p>
                     <p><strong>Elephant Ride:</strong></p>
                     <p class="paragraph">The first and more preferred mode is on Elephant’s back. Elephant rides allow you to get up very close to the wild animals and make you feel totally one with nature due to the lack of any sound of an engine.</p>
