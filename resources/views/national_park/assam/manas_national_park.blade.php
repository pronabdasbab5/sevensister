                     <h2 class="text-center">Manas National Park<br>
                     Manas is probably a site of what the earth looked like before the arrival of man !!!
                     </h2>
                     <p class="paragraph">In 1985, the Manas Wildlife Sanctuary was inscribed as World Heritage Site. In 1989, Manas acquired the status of a Biosphere reserve. It extends over an area of 2837 Sq. Km from Sankosh river in the west to Dhansiri river in the east, with a core area of 500 Sq. Km. of the National park, which declared in 1990. </p>
                     <p class="paragraph">Lying on the foothills of the Himalaya, Manas is the most stunning pristine wildlife habitat in India, comparable to the best in the world in the beauty of its spectacular landscape. It is also a UNESCO Natural World Heritage (in danger) site, a Project Tiger Reserve, an Elephant Reserve and a Biosphere Reserve - a unique distinction. This Brahmaputra Valley semi-evergreen forest Terrestrial Eco-region is also the richest in species of all Indian wildlife areas and the only known home for the rare and endangered Assam Roofed Turtle, Hispid Hare, Golden Langur and Pygmy Hog. </p>
                     <p class="paragraph">Manas is the closest I have come to seeing paradise on earth in my life - but that was 25 years ago. Today, Manas looks like an aged diva wearing rags, though I think I still caught the familiar sparkle in the eye.</p>
                     <p class="paragraph">The focus point of Manas National Park is the enchanting Manas River, named after the serpent goddess Manasa. It is the largest Himalayan tributary of the mighty Brahmaputra. Coming down the Bhutan Hills from the north, the crystal clear waters of the Manas river runs through the heart of the 500 sq. km core area of Manas Park. The main tourist spot of Mothanguri, on the northern border of Manas with Bhutan, is situated on the banks of this river.</p>
                     <p class="paragraph">Situated in the north bank of the Brahmaputra river, in Assam, Manas lies on the international border with Bhutan. It is bounded on the north by the Royal Manas National Park in Bhutan, on the south by populous North Kamrup district and on both east and west by buffer forest reserves which are part of 2,840 sq. Km Manas Tiger Reserve.</p>
                     <p class="paragraph">The Manas River flows through the west of the park, where it splits into two separate rivers, the Beki and Bholkaduba. These and five small rivers drain the Reserve which lies on a wide low-lying alluvial terrace below the foothills of the outer Himalaya.</p>
                     <p class="paragraph">Limestone and sandstone form the bedrock of the savanna area in the north while the grasslands in the south consist of deep deposits of fine alluvium.</p>
                     <p><strong>Vegetation:</strong></p>
                     <p class="paragraph">The Burma Monsoon Forests of Manas lie on the borders between the Indo-Gangetic and Indo-Malayan bio-geographical realms and is part of the Brahmaputra Valley Biogeographic Province. The combination of Sub-Himalayan Bhabar Terai formation with riverine succession leading up to Sub-Himalayan mountain forest makes it one of the richest biodiversity areas in the world. </p>
                     <p class="paragraph">Two major biomes are represented in Manas ~ the grassland biome and the forest biome. 
                     </p>
                     <p>The main vegetation types are: 
                      <ul >
                        <li>i) Sub-Himalayan Light Alluvial Semi-Evergreen forests in the northern parts,</li>
                        <li>ii) East Himalayan mixed Moist and Dry Deciduous forests (the most common type), </li>
                        <li>iii) Low Alluvial Savanna Woodland, and</li>
                        <li class="paragraph">iv) Assam Valley Semi-Evergreen Alluvial Grasslands which cover almost 50% of the Park. Much of the riverine dry deciduous forest is at an early successional stage. It is replaced by moist deciduous forest away from water courses, which is succeeded by semi-evergreen climax forest in the northern part of the park. A total of 543 plants species have been recorded from the core zone. Of these, 374 species are dicotyledons (including 89 trees), 139 species monocotyledons and 30 are Pteridophytes and Gymnosperms.</li>
                     </ul>
                  </p>
                     <p><strong>Fauna:</strong></p>
                     <p class="paragraph">Manas is the melting point of the west and the east, with many species at the westernmost and easternmost point of their range representing a gateway for species exchanges between the typically Indian and Malayan' faunas.</p>
                     <p class="paragraph">A total of 55 mammals, 50 reptiles and three amphibians have been recorded, several species being endemic. Manas contains 21 of India's Schedule I mammals and at least 33 of its animals listed as threatened, by far the greatest number of any protected area in the country. Some, like the Assam Roofed turtle Kachuga sylhetensis, Golden Langur Presbytis geei, Hispid Hare Caprolagus hispidus, Pygmy Hog Sus salvanius and the only pure strain of Asiatic Wild Buffalo Bubalus arnee, are only found/best seen here.</p>
                     <p><strong>Bird Life:</strong></p>
                     <p class="paragraph">The diverse habitat of Manas is ideal home for a variety of specialized birds. Manas boasts the largest population of the endangered Bengal Florican in the world and is also a great place to see the Great Hornbill. The National Park lists around 380 species and the adjoining hilly terrain in Bhutan can easily add a hundred birds to that total. Good birds to look for are Greater Adjutant, Black-tailed Crake, Red-headed Trogon, Swamp Francolin, Wreathed and Rufous-necked Hornbill, Marsh and Jerdon's Babblers, Pied Harrier, Rufous-rumped and Bristled Grassbirds, Hodgson's Bushchat, Rufous-vented Laughingthrush, Finn's Weaver, Ibisbill and a variety of foothills species.</p>
                     <p class="text-center"><strong>How to reach Manas</strong></p>
                     <p class="paragraph"><strong>By Air – </strong>Nearest Airport is Guwahati (180 kms / 5 hrs drive approx.). Regular flight operates from Bagdogra, Kolkata, New Delhi, Mumbai, Bangalore, Chennai, Jaipur, Hyderabad, Goa, Dibrugarh, Jorhat by Indian Airlines (Air India), Jet Airways, Jetlite, Indigo, & Go Air. Drukair connects Bangkok & Paro (Bhutan) with Guwahati twice a week.</p>
                     <p class="paragraph"><strong>By Road – </strong>Manas is 176 kms from Guwahati. The National Highway No. 31 joins Barpeta Road (not Barpeta town) which is 22 kms to Bansbari. The journey from Guwahati by road is 5 hrs. Approx.</p>
                     <p class="paragraph"><strong>By Train – </strong>Guwahati is gateway to the North East India. All major cities of India are well connected to Guwahati including Rajdhani Express. There are several trains stops at Barpeta Road Railway Station, which is 22 kms to Bansbari</p>

                     <p><strong>Best Season:</strong></p>
                     <p class="paragraph">The best season of Manas National Park are from November to April.</p>
                     <p class="paragraph">The Park is open daily from  November to April i.e., for 6 (six) months, unless due to the rains in any exceptional year the park has to be closed earlier by the management for safety reasons.</p>
                     <p class="paragraph"><strong>Partially open – </strong>Park remains open partially on the months from May & October.</p>
                     <p class="paragraph"><strong>Closed – </strong>The park remains closed June to September.</p>
                      <p class="paragraph"><strong>Excursions – </strong>Elephant ride and Jeep safari.</p>
                       <p class="paragraph"><strong>Hours – </strong>The closing hours are scheduled to accord roughly with the seasonal variation in climate, as well as with the length of the day at different months, as tourists can enter the park only during the day time. Though in India is all on one time Zone, sun rise and sun set time are early in Manas or Assam (or in North East India) than Delhi, Gujarat and other states of India. The sun sets in Manas or Assam at around 4.30 pm in the month of November and in March / April it sets around 5.30 pm.</p>
