                      <p class="para">Assam, Arunachal Pradesh, Manipur, Meghalaya, Mizoram, Nagaland and Tripura are the original Seven Sisters. Each state has its own unique history and culture, and for a traveller, the region is a repository of rare experiences. It’s fascinating that even many Indians from outside the north-east region know so little about the place. If you’ve travelled across India but have never visited the north-east, here are the reasons to do so. Previously known as the Seven Sisters, north-east India became richer with the addition of an eighth, Sikkim in 1975. Before joining India as its 22nd state, Sikkim was a small Himalayan monarchy.
                      </p>
                      <p class="para">
                        <strong>Eco-friendly destinations:
                        </strong>For the conscious traveller, north-east India offers many eco-friendly destinations to choose from. There’s Khonoma in Nagaland where the villagers banded together to stop unchecked deforestation by outsiders. Mawlynnong in Meghalaya was awarded the cleanest village in Asia and where all guesthouses are constructed of bamboo. The Apatani tribe of Ziro Valley in Arunachal Pradesh have developed their own unique sustainable methods of farming. Then there’s the breathtaking Khangchendzonga National Park in Sikkim. If you believe in the concept of green travel, then this is an absolutely unmissable destination.
                      </p>
                      <p class="para">
                        <strong>See the endangered one-horned rhino:
                        </strong>One-horned rhinos were almost extinct by 1975, with only 600 of them remaining in the jungles of Nepal and India. Hunting and poaching for their horns were the two main causes behind the decline. But with dedicated efforts, the population of this endangered species has gone up to 3,500 and more than half of them reside in Kaziranga National Park in Assam. The national park also has the highest density of tigers in the world. Take a safari tour here and while you meet elephants, wild water buffaloes, swamp deers and a wide variety of birds, you can also admire the enormous work that Kaziranga National Park has put in towards wildlife conservation.
                      </p>
                      <p class="para">
                        <strong>Rare gastronomic experience:
                        </strong> The cuisines from the eight north-eastern states couldn’t be more different from that of mainland Indian food. For one, the use of spices is kept to a minimum. Instead ingredients like soy bean and bamboo shoot in their fermented forms, add flavour to the dishes. During his trip to Nagaland, even Gordon Ramsay couldn’t believe he was dining in India. But it would be wrong to classify the cuisine from this region as one entity because each state varies in their style of cooking. Sikkim, for instance, is largely influenced by Tibetan and Nepali cuisine. However, what can be said with certainty is that gastronomes will be fascinated with their finds when travelling in this part of the country.
                      </p>
                      <p class="para">
                        <strong>Spiritual awakening:
                        </strong> Arunachal Pradesh, is the easternmost state of India. It is therefore called as the ‘Land of the rising sun’. The awe-inspiring natural landscapes of the state are dotted with several seminal Buddhist monasteries. The most important of them all is Tawang Monastery, founded in 1680. It also happens to be the largest monastery in India. When at Tawang, do indulge in some warm cups of coffee at the Dharma Coffee House and Library. Then there’s Urgelling Monastery which is the birthplace of the sixth Dalai Lama. These monasteries create a meditative atmosphere ideal for spiritual introspection. The sweeping views of snow-capped mountains is an additional luxury.
                      </p>
                      <p class="para">
                        <strong>Challenging trekking routes: 
                        </strong> For the adventurous soul, north-east India presents some challenging trekking routes accompanied by spectacular views. The Bailey Trail Trek in Arunachal Pradesh follows the ancient trade route connecting India, Tibet and Myanmar. The Goechala Trek in West Sikkim is considered by many to be the closest comparison to the famed trekking routes of Nepal. Dzukou Valley, Beyul of Pemako, Green Lake, Reiek Mountain and Unakoti are some of the other treks you can explore in this region.
                      </p>
                      <p class="para">
                        <strong>Sikkim, India’s first and only organic state: 
                        </strong>  It was in 2004 that Sikkim embarked upon a journey to become a fully organic state. It managed to achieve this ambitious feat 12 years later. Since then, the least populous and second smallest state of India, has become a shining jewel in India’s crown. There are many rural villages in Sikkim where travellers are welcome to experience hands-on organic farming while living with the locals in their humble homestays. Lachung, Lachen, Denchung and Kaluk are some of the popular villages you can visit.
                      </p>
                      <p class="para">
                        <strong>Rare gastronomic experience:  
                        </strong>The cuisines from the eight north-eastern states couldn’t be more different from that of mainland Indian food. For one, the use of spices is kept to a minimum. Instead ingredients like soy bean and bamboo shoot in their fermented forms, add flavour to the dishes. During his trip to Nagaland, even Gordon Ramsay couldn’t believe he was dining in India. But it would be wrong to classify the cuisine from this region as one entity because each state varies in their style of cooking. Sikkim, for instance, is largely influenced by Tibetan and Nepali cuisine. However, what can be said with certainty is that gastronomes will be fascinated with their finds when travelling in this part of the country.
                      </p>
                      <p class="para">
                        <strong>Eclectic culture: 
                        </strong> The culture of north-eastern states are characterised by the diverse ethnic groups settled in the region. Each tribe has its own distinct custom, cuisine, attire and dialect. One of these tribes is the Konyak, the last remaining headhunter clans of the region. Konyaks were once renowned for decapitating their enemies and proudly displaying them on their return to the village. With time, these age old customs are silently vanishing, but what the Konyaks teach us is that there’s a wealth of cultural experiences hidden within the mystical hills of north-east India.
                      </p>
                      <p class="para">
                        <strong>Memorable road trips: 
                        </strong>  This offbeat destination in India also has some great routes for an epic road trip. The winding roads, often rugged in many places, can pose a challenge to any driver. But that’s where all the adventure lies. Along the way, you’ll frequently come across pristine forests, rolling hills, national parks, gushing rivers, sprawling tea gardens, stunning waterfalls and many such natural grandeurs. Taking a road trip is truly one of the best ways to experience north-east India.
                      </p>
                      <p class="para">
                        <strong>Rich handicrafts heritage: 
                        </strong>
                        Northeast India is home to a large number of tribes and these tribes have a rich Handicraft tradition of their own. Bamboo works, especially, are widespread in the region. Assam, Manipur, Mizoram, Nagaland and Tripura have some very skilled bamboo craftsmen who make beautiful screens, lamp stands, furniture and baskets among other things. Manipur is recognized for gold and gold plated jewellery, Assam and Meghalaya for their silk textiles and Sikkim for carpet weaving. Pottery is also a commonly practiced craft in some of these states.
                      </p>
                      <p class="para">
                        <strong>Meet future Olympians: 
                        </strong>
                        Northeast India is well-known for producing champion sportspeople, especially female athletes. Boxer Mary Kom from Manipur is the most famous Olympian from the region. She was the only Indian woman boxer to qualify for the 2012 Summer Olympics and ended up winning the bronze medal in the competition. At the 2016 Rio Olympics, Dipa Karmakar from Tripura made history as the first Indian female gymnast to compete in the Olympics. Anshu Jamsenpa from Arunachal Pradesh is another renowned sportsperson from north-east India. She is the first woman to scale Mount Everest twice within five days.
                      </p>
                      <p class="para">
                        <strong>Find musical inspiration: 
                        </strong>
                        Northeast India is home to some of the best musicians in the country. However, most of them are very underrated. Western music, especially, is the source inspiration for most people in the region. Shillong, the capital town of Meghalaya, is also known as the Rock Music Capital of India. Legendary musician Lou Majaw hails from the state of Meghalaya, so does Soulmate, one of India’s best blues band. If you want to experience the brilliant music coming from north-east India, make sure to attend the Ziro Festival of Music in Arunachal Pradesh, which is a highly sought-after musical fixture in the country.
