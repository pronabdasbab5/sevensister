              <p class="paragraph">The North East India comprises of the seven sister states of Assam, Arunachal Pradesh, Manipur, Meghalaya, Mizoram,  Nagaland  and Tripura.  They form part of the   East Himalayan  region which extends from Sikkim eastwards  and   embraces  the  Darjeeling Hills  of West  Bengal. The   location  of  the  region is  strategically important as   it has  international  borders  with Bangladesh, Bhutan,   China, Myanmar and Tibet. The area is characterised by rich  bio-diversity, heavy  precipitation  and high seismicity. It is endowed with forest wealth and is  ideally  suited to  produce a  whole range of plantation   crops, spices, fruits and vegetables and flowers and herbs. The rich natural beauty, serenity and exotic flora and fauna of the area are invaluable resources for the development of  eco-tourism. Total  area of the  region is about 2,55,168 Sq. Km. All the seven states are members  of the  North East Council, organized on 1st  August 1972 and  within the Jurisdiction of Guwahati High Court.
